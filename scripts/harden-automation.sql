  run_id uuid NOT NULL REFERENCES public.automation_sync_runs(id) ON DELETE CASCADE,
  locked_at timestamptz NOT NULL DEFAULT now(),
  expires_at timestamptz NOT NULL
);

ALTER TABLE public.automation_sync_runs ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.automation_sync_locks ENABLE ROW LEVEL SECURITY;

