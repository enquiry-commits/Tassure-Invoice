-- QuickBooks must be keyed by immutable QB IDs. DocNumber is display data and
-- QuickBooks can contain duplicates or have a number edited later.
ALTER TABLE public.quickbooks_invoices
  ADD COLUMN IF NOT EXISTS qb_invoice_id text,
  ADD COLUMN IF NOT EXISTS qb_customer_id text,
  ADD COLUMN IF NOT EXISTS last_seen_sync_run uuid;

ALTER TABLE public.quickbooks_invoice_items
  ADD COLUMN IF NOT EXISTS qb_line_id text,
  ADD COLUMN IF NOT EXISTS qb_customer_id text,
  ADD COLUMN IF NOT EXISTS last_seen_sync_run uuid,
  ADD COLUMN IF NOT EXISTS classification_source text,
  ADD COLUMN IF NOT EXISTS period_parse_status text;

DO $$
DECLARE
  item record;
BEGIN
  FOR item IN
    SELECT con.conname, rel.relname
    FROM pg_constraint con
    JOIN pg_class rel ON rel.oid = con.conrelid
    JOIN pg_namespace ns ON ns.oid = rel.relnamespace
    WHERE ns.nspname = 'public'
      AND rel.relname IN ('quickbooks_invoices', 'quickbooks_invoice_items')
      AND con.contype = 'u'
  LOOP
    -- Replace all legacy DocNumber-based uniqueness with immutable QB IDs.
    IF item.conname NOT IN (
      'quickbooks_invoices_company_qb_id_key',
      'quickbooks_invoice_items_company_qb_line_key'
    ) THEN
      EXECUTE format('ALTER TABLE public.%I DROP CONSTRAINT %I', item.relname, item.conname);
    END IF;
  END LOOP;
END;
$$;

DO $$
DECLARE
  item record;
BEGIN
  FOR item IN
    SELECT schemaname, indexname
    FROM pg_indexes
    WHERE schemaname = 'public'
      AND tablename IN ('quickbooks_invoices', 'quickbooks_invoice_items')
      AND indexdef ILIKE 'CREATE UNIQUE INDEX%'
      AND indexname NOT IN (
        'quickbooks_invoices_pkey',
        'quickbooks_invoice_items_pkey',
        'quickbooks_invoices_company_qb_id_key',
        'quickbooks_invoice_items_company_qb_line_key'
      )
  LOOP
    EXECUTE format('DROP INDEX IF EXISTS %I.%I', item.schemaname, item.indexname);
  END LOOP;
END;
$$;

