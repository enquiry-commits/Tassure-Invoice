  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conname = 'quickbooks_invoice_items_company_qb_line_key'
  ) THEN
    ALTER TABLE public.quickbooks_invoice_items
      ADD CONSTRAINT quickbooks_invoice_items_company_qb_line_key
      UNIQUE (qb_company, qb_invoice_id, qb_line_id);
  END IF;
END;
$$;

