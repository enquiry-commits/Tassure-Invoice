    SELECT 1 FROM pg_constraint
    WHERE conname = 'quickbooks_invoices_company_qb_id_key'
  ) THEN
    ALTER TABLE public.quickbooks_invoices
      ADD CONSTRAINT quickbooks_invoices_company_qb_id_key
