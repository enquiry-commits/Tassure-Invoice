CREATE EXTENSION IF NOT EXISTS pgcrypto;

ALTER TABLE public.companies
  ADD COLUMN IF NOT EXISTS fye_day smallint
  CHECK (fye_day BETWEEN 1 AND 31);

ALTER TABLE public.ar_reminder
  ADD COLUMN IF NOT EXISTS company_id bigint REFERENCES public.companies(id);

UPDATE public.ar_reminder ar
SET company_id = company.id
FROM public.companies company
WHERE ar.company_id IS NULL
  AND lower(trim(ar.entity_name)) = lower(trim(company.company_name));

CREATE UNIQUE INDEX IF NOT EXISTS ar_reminder_company_cycle_key
  ON public.ar_reminder (company_id, fye_year, fye_month)
  WHERE company_id IS NOT NULL;

