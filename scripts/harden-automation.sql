-- Replace one set of TeamWork ND appointments in one database transaction.
-- The delete is rolled back automatically if any incoming row is invalid.
CREATE OR REPLACE FUNCTION public.replace_nd_appointments(
  p_nd_ids bigint[],
  p_rows jsonb
)
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  inserted_count integer := 0;
BEGIN
  DELETE FROM public.nd_appointments WHERE nd_id = ANY (p_nd_ids);

  INSERT INTO public.nd_appointments (
    nd_id, company_name, sub_role, appointment_date, cessation_date
  )
  SELECT
    item.nd_id,
    item.company_name,
    'Nominee Director',
    item.appointment_date,
    item.cessation_date
  FROM jsonb_to_recordset(COALESCE(p_rows, '[]'::jsonb)) AS item(
    nd_id bigint,
    company_name text,
    appointment_date date,
    cessation_date date
  )
  WHERE item.nd_id = ANY (p_nd_ids)
    AND item.company_name IS NOT NULL;

  GET DIAGNOSTICS inserted_count = ROW_COUNT;
  RETURN inserted_count;
END;
$$;
